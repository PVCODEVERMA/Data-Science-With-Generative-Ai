# Required arguments: (the function calculate returns the sum of two arguments a and b )

def calculate(a,b):
    """
    Returns the sum of two argument , a and b.

    Args:
        a (float or int): First argument.
        b (float or int): Second argument.
    returns:
        float or int: Sum of a and b.
    """
    return a + b

result = calculate(5,3)
print(f"The sum of 5 and 3 in {result}")
  